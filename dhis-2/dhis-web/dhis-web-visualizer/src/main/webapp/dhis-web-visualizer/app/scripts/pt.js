var getDims = function(pt) {
	var response = pt.response,
		settings = pt.settings,
		getUniqueColsArray,
		getUniqueRowsArray;

	getUniqueColsArray = function(response, settings) {
		var a = [];

		for (var i = 0, header; i < settings.col.length; i++) {
			header = settings.col[i];
			a.push(response.nameHeaderMap[header].items);
		}

		return a;
	};

	getUniqueRowsArray = function(response, settings) {
		var a = [];

		for (var i = 0, header; i < settings.row.length; i++) {
			header = settings.row[i];
			a.push(response.nameHeaderMap[header].items);
		}

		return a;
	};

	// aUniqueCols ->  [[p1, p2, p3], [ou1, ou2, ou3, ou4]]

	return {
		cols: extendDims(getUniqueColsArray(response, settings)),
		rows: extendDims(getUniqueRowsArray(response, settings))
	};
};

var extendRowDims = function(rows) {
	var all = rows.items.all,
		allObjects = [];

	for (var i = 0, allRow; i < all.length; i++) {
		allRow = [];

		for (var j = 0; j < all[i].length; j++) {
			allRow.push({
				id: all[i][j]
			});
		}

		allObjects.push(allRow);
	}

	for (var i = 0; i < allObjects.length; i++) {
		for (var j = 0, object; j < allObjects[i].length; j += rows.span[i]) {
			object = allObjects[i][j];
			object.rowSpan = rows.span[i];
		}
	}

	rows.items.allObjects = allObjects;
};

var getTablePanel = function(pt) {
	return Ext.create('Ext.panel.Panel', {
		renderTo: Ext.get('pivottable'),
		layout: {
			type: 'table',
			columns: pt.config.cols.size + pt.config.rows.dims + 1
		},
		defaults: {
			baseCls: 'td'
		}
	});
};

var getEmptyItem = function(pt) {
	return {
		colspan: pt.config.rows.dims,
		rowspan: pt.config.cols.dims,
		baseCls: 'empty'
	};
};

var getColItems = function(pt) {
	var response = pt.response,
		cols = pt.config.cols,
		colItems = [];

	for (var i = 0, dimItems, colSpan; i < cols.dims; i++) {
		dimItems = cols.items.gui[i];
		colSpan = cols.span[i];

		for (var j = 0, id; j < dimItems.length; j++) {
			id = dimItems[j];
			colItems.push({
				html: response.metaData[id],
				colspan: colSpan,
				baseCls: 'dim'
			});

			if (i === 0 && j === (dimItems.length - 1)) {
				colItems.push({
					html: 'Total',
					rowspan: cols.dims,
					baseCls: 'dimtotal'
				});
			}
		}
	}

	return colItems;
};

var getRowItems = function(pt) {
	var response = pt.response,
		rows = pt.config.rows,
		cols = pt.config.cols,
		size = rows.size,
		dims = rows.dims,
		allObjects = rows.items.allObjects,
		dimHtmlItems = [],
		valueItems = [],
		valueHtmlItems = [],
		totalRowItems = [],
		totalRowHtmlItems = [],
		totalColItems = [],
		totalColHtmlItems = [];

	// Value items
	for (var i = 0, row; i < size; i++) {
		row = [];

		for (var j = 0, id, value, row; j < pt.config.cols.size; j++) {
			id = cols.ids[j] + rows.ids[i];
			value = parseFloat(response.idValueMap[id]);
			row.push(value);
		}

		valueItems.push(row);
	}

	// Value html items
	for (var i = 0, row; i < valueItems.length; i++) {
		row = [];

		for (var j = 0, id, value, cls; j < valueItems[i].length; j++) {
			id = cols.ids[j] + rows.ids[i];
			value = valueItems[i][j];
			cls = value < 333 ? 'bad' : (value < 666 ? 'medium' : 'good'); //simplistic legendset

			row.push({
				id: id,
				value: value,
				html: value.toString(),
				baseCls: 'value',
				cls: cls
			});
		}

		valueHtmlItems.push(row);
	}

	// Total row items
	for (var i = 0, sum; i < valueItems.length; i++) {
		sum = Ext.Array.sum(valueItems[i]);
		totalRowItems.push(sum);
	}

	// Total row html items
	for (var i = 0, value; i < totalRowItems.length; i++) {
		value = totalRowItems[i];

		totalRowHtmlItems.push({
			value: value,
			html: value.toString(),
			baseCls: 'valueTotal'
		});
	}







	for (var i = 0; i < size; i++) {
		for (var j = 0, object; j < dims; j++) {
			object = allObjects[j][i];

			if (object.rowSpan) {
				dimHtmlItems.push({
					html: response.metaData[object.id],
					rowspan: object.rowSpan,
					baseCls: 'dim'
				});
			}
		}

		// Values
		for (var j = 0, id, value, cls; j < pt.config.cols.size; j++) {
			id = cols.ids[j] + rows.ids[i];
			value = response.idValueMap[id];
			cls = parseFloat(value) < 333 ? 'bad' : (parseFloat(value) < 666 ? 'medium' : 'good'); //simplistic legendset

			dimHtmlItems.push({
				id: id,
				value: value,
				html: value,
				baseCls: 'value',
				cls: cls
			});
		}
	}

	return dimHtmlItems;
};

